package Study06;

public class DoWhile {

	public static void main(String[] args) {
		
		// 1 ~ 10
		int a = 1;
		do {
			System.out.println(a);
			a++;
		} while(a <= 10);

		System.out.println();
		System.out.println();
		
		int b = 1;
		while(b<=10) {
			System.out.println(b);
			b++;
		}
		
		

	}

}
