package study02;

public class Test4 {

	public static void main(String[] args) {
		//비교연산자 (==, !=, >, <, >=, <=)
		
		//같다
		System.out.println(100==100);
		System.out.println(100==80);
		System.out.println("================");
		
		//같지 않다
		System.out.println(100!=100);
		System.out.println(100!=80);
		System.out.println("================");
		
		//크다
		System.out.println(100>100);
		System.out.println(100>90);
		System.out.println(80>90);
		System.out.println("================");
		
		//작다
		System.out.println(100<90);
		System.out.println(100<200);
		System.out.println("================");
		
		//크거나 같다
		System.out.println(100>=100);
		System.out.println(100>=80);
		System.out.println(100>=200);
		System.out.println("================");
		
		//작거나 같다
		System.out.println(100<=100);
		System.out.println(100<=80);
		System.out.println(100<=200);
		System.out.println("================");
		
		
		
		
		

	}

}
